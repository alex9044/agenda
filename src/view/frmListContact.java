    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
 
        String option  = String.valueOf(cbx.getSelectedItem());
        String condition = "";
        String search = txtField.getText();
        
        if(option.equals("NOMBRE")){
            condition = " where name like '%" +search+"%'";
        }else if (option.equals("ID")){
            condition = " where id= '"+search+"'";
        }
        
        Table.setModel(abm.insertTable(condition));
        txtField.setText("");
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String code = String.valueOf(Table.getValueAt(Table.getSelectedRow(), 0));
        if (abm.deleteContact(code)== true) {
            Table.setModel(abm.insertTable(""));
            JOptionPane.showMessageDialog(null, "Registro eliminado");
        } else {
            JOptionPane.showMessageDialog(null, "Error al eliminar");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

