    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
       frm.setVisible(true);
       String code = String.valueOf(Table.getValueAt(Table.getSelectedRow(), 0));
       if(abm.getContact(code, contact) == true){  
        frm.txtId.setText(String.valueOf(contact.getId()));
        frm.txtName.setText(contact.getName());
        frm.txtPhone.setText(contact.getPhone());
        frm.txtAdress.setText(contact.getAdress());
        frm.conditions = "change";
        Table.setModel(abm.insertTable(""));
       }
       
    }//GEN-LAST:event_btnChangeActionPerformed

